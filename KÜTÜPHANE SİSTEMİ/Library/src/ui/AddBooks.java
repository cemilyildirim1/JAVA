    public void populateTable(){
        model = (DefaultTableModel)tblBooks.getModel();
        model.setRowCount(0);
        try {
            // Books verilerini çekme
            BooksDAO booksDAO = new BooksDAO();
            ArrayList<Books> booksList = booksDAO.getAll();
            ArrayList<Books> books = booksList;
            for(Books book : books){
                    Object [] row = {book.getId(),book.getBook_name(),book.getWriter_name(),book.getPage_number(),book.getBook_type()};
                 model.addRow(row);
            }
           
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
    public ArrayList<Books> getBook() throws SQLException{
            Connection connection = null;
            Statement statement=null;
            ResultSet resultSet;
            
            ArrayList<Books> books = new ArrayList<>();
            
        try{
            connection = DbHelperSingleton.getConnection();
            System.out.println("Baglanti basarili.");
            statement = connection.createStatement();
            resultSet = statement.executeQuery("select * from books");
            
            while(resultSet.next()){
                books.add(new Books(
                        resultSet.getInt("id"),
                        resultSet.getString("book_name"),
                        resultSet.getString("writer"),
                        resultSet.getInt("page_number"),
                        resultSet.getString("type")
                )
                );
            }
            
        }catch(SQLException exception){
           DbHelperSingleton.showErrorMessage(exception);
        }finally{
            statement.close();
            connection.close();
        }
        
        return books;
    }

